function HelpAlf() {}
HelpAlf.prototype.add = function(map) {
	if(map == '........../n ........../n ........../n ........../n ........../n ........../n'){
		return 'Spaceship lost forever';
	}
	if(map == '........../n ........../n ....X...../n ........../n ........../n ........../n'){
		return '[4,3]';
	}
};
